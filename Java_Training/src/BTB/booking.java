package BTB;

public class booking {
	
	public String bid, rid;
	public int no_of_seats;
	public double totalAmount;
	
	public booking(String bid, String rid, int no_of_seats, double totalAmount) {
		
		this.bid = bid;
		this.rid = rid;
		this.no_of_seats = no_of_seats;
		this.totalAmount = totalAmount;
		
	}
	
}
